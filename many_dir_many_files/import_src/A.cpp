namespace tmp {
	template <typename A>
	A multi(const A &a1, const A &a2)
	{
	   return a1 * a2;
	}

	template <typename A>
	A devide(const A &a1, const A &a2)
	{
	   return a1 / a2;
	}

	template <typename A>
	A minus(const A &a1, const A &a2)
	{
	   return a1 - a2;
	}

	template <typename A>
	A add(const A &a1, const A &a2)
	{
	   return a1 + a2;
	}
}

